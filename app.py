@app.route('/api/admin/prompts', methods=['POST'])
def set_prompts():
    if request.headers.get('X-Admin-Password') != ADMIN_PASSWORD:
        return jsonify({'error': 'Unauthorized'}), 401
    data = request.get_json()
    if not data:
        return jsonify({'error': 'Invalid JSON'}), 400
